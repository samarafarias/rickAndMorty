class Character {
    constructor(id, name, status, species, gender, origin, location, image, episode) {
        this.id = id;
        this.name = name;
        this.status = status;
        this.species = species;
        this.gender = gender;
        this.origin = origin;
        this.location = location;
        this.image = image;
        this.episode = episode;
    }
}

module.exports = Character;
